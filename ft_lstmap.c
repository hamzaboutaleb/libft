#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*new_node;
	void	*data;

	new_node = NULL;
	new_lst = NULL;
	while (lst)
	{
		data = f(lst->content);
		new_node = ft_lstnew(data);
		if (!new_node)
		{
			del(data);
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_node);
		lst = lst->next;
	}
	return (new_lst);
}
