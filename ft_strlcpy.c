unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int	count;
	unsigned int i;

	count = 0;
	while (src[count])
		count++;
	if (size == 0)
		return (count);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (count);
}