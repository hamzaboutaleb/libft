#include "libft.h"

char	*ft_strdup(char *src)
{
	int		slen;
	char	*res;
	int		i;

	i = 0;
	slen = ft_strlen(src);
	res = (char *)malloc(slen + 1);
	if (res == NULL)
		return (NULL);
	while (src[i])
	{
		res[i] = src[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
